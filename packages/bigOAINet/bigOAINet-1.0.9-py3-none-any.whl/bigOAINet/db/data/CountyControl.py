import mxupy as mu
import bigOAINet as bigo

class CountyControl(mu.EntityXControl):
    class Meta:
        model_class = bigo.County
    
    def get_id(self, name):
        """
        获取编号

        参数:
            name (str): 区名称

        返回:
            int: 区编号，如果未找到则返回-1
        """
        def _do(im):
            mc = self.model_class
            im.data = -1
            im.data = mc.get(mc.name.contains(name)).countyId

        return self.run(_do)

    def add_county(self, id, cityId, name, enname):
        """
        添加（用于初始化数据）

        参数:
            id (int): 区域id
            cityId (int): 城市id
            name (str): 名称
            enName (str): 英文名称

        返回:
            IM: 返回操作结果
        """
        def _do(im):
            
            mc = self.model_class
            
            if mc.select().where(mc.countyId == id).exists():
                im.success = False
                im.msg = f'已经存在 countyId 为 {id} 的记录。'
                return
            
            c = {
                'countyId':id,
                'cityId':cityId,
                'name':name,
                'enname':enname
            }    
            im.data = mc.create(**c)

        return self.run(_do)
    
    
