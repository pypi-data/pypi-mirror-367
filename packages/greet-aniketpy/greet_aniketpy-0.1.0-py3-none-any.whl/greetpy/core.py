def greet(name: str) -> str:
    """Return a standard greeting."""
    return f"Hello, {name}!"

def greet_formal(name: str) -> str:
    """Return a formal greeting."""
    return f"Good day, Mr. {name}."

def greet_casual(name: str) -> str:
    """Return a casual greeting."""
    return f"Hey {name}, what's up?"